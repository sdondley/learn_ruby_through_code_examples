#!/usr/bin/env ruby

# Constant variables do not have a sigil and begin with an uppercase letter

# Here we initialize a global variable to an integer object that has
# a value of '7'
#
# Although the characters after the first one can be lowercase, it is considered
# best practice to use all caps
THIS_IS_CONSTANT_VARIABLE = 7

# Print the contents of the variable to the screen
puts THIS_IS_CONSTANT_VARIABLE
